#!/usr/bin/env node

import { existsSync, readFileSync, writeFileSync, mkdirSync, chmodSync, lstatSync, unlinkSync, readdirSync, renameSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { createInterface } from 'node:readline';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const TEMPLATES_DIR = join(__dirname, '..', 'templates');

const LEFTHOOK_VERSION = '2.1.4';

// ── formatting ──────────────────────────────────────────────────────────────

const green = (s) => `\x1b[32m${s}\x1b[0m`;
const yellow = (s) => `\x1b[33m${s}\x1b[0m`;
const red = (s) => `\x1b[31m${s}\x1b[0m`;
const dim = (s) => `\x1b[2m${s}\x1b[0m`;
const bold = (s) => `\x1b[1m${s}\x1b[0m`;

const ok = (msg) => console.log(`  ${green('✓')} ${msg}`);
const skip = (msg) => console.log(`  ${dim('–')} ${dim(msg)}`);
const warn = (msg) => console.log(`  ${yellow('!')} ${msg}`);
const fail = (msg) => console.log(`  ${red('✗')} ${msg}`);

// ── helpers ─────────────────────────────────────────────────────────────────

function prompt(question) {
  const rl = createInterface({ input: process.stdin, output: process.stdout });
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim().toLowerCase());
    });
  });
}

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function writeJson(path, data) {
  writeFileSync(path, JSON.stringify(data, null, 2) + '\n');
}

function semverCompare(a, b) {
  const pa = a.replace(/^[\^~>=<]*/g, '').split('.').map(Number);
  const pb = b.replace(/^[\^~>=<]*/g, '').split('.').map(Number);
  for (let i = 0; i < 3; i++) {
    if ((pa[i] || 0) < (pb[i] || 0)) return -1;
    if ((pa[i] || 0) > (pb[i] || 0)) return 1;
  }
  return 0;
}

function detectPackageManager(cwd) {
  if (existsSync(join(cwd, 'pnpm-lock.yaml'))) return 'pnpm';
  if (existsSync(join(cwd, 'yarn.lock'))) return 'yarn';
  if (existsSync(join(cwd, 'bun.lockb')) || existsSync(join(cwd, 'bun.lock'))) return 'bun';
  return 'npm';
}

const GITIGNORE_SECTION = `# Agent config symlinks (generated by re-agent)
.claude
**/CLAUDE.md
.github/copilot-instructions.md
.cursor/rules/main.mdc
.planning/codebase`;

// Matches old comment or re-agent comment — captures the block to the next blank line or EOF
const GITIGNORE_PATTERN = /# Agent config symlinks \(generated by [^)]+\)\n(?:[^\n]+\n?)*/;

const AGENTS_TEMPLATE = `# Agent Instructions

> **This file is the single source of truth for AI coding agent configuration.**
>
> [re-agent](https://npmjs.com/package/re-agent) symlinks it to each tool's expected location:
>
> | Tool | Symlink |
> |------|---------|
> | Claude Code | \`CLAUDE.md\` |
> | GitHub Copilot | \`.github/copilot-instructions.md\` |
> | Cursor | \`.cursor/rules/main.mdc\` |
>
> Edit this file — all tools stay in sync automatically.

## Project

<!-- Describe your project, stack, and key context for AI agents -->

## Conventions

<!-- Coding conventions, naming, style, and architectural rules -->

---

### The \`.agent/\` directory

Shared agent resources live in \`.agent/\` and are symlinked into tool-specific locations:

\`\`\`
.agent/
\u251c\u2500\u2500 skills/     \u2192 .claude/commands/    (custom slash commands)
\u2514\u2500\u2500 codebase/   \u2192 .planning/codebase   (codebase documentation)
\`\`\`

These directories are version-controlled. Add custom commands as subdirectories of
\`.agent/skills/\` and codebase documentation to \`.agent/codebase/\`.
`;

// ── init ────────────────────────────────────────────────────────────────────

async function init() {
  const cwd = process.cwd();
  console.log(bold('\nre-agent init\n'));

  // ── 1. package.json ───────────────────────────────────────────────────────

  const pkgPath = join(cwd, 'package.json');

  if (!existsSync(pkgPath)) {
    const answer = await prompt(
      `  ${yellow('No package.json found.')} Run ${bold('npm init -y')}? (y/n) `
    );
    if (answer === 'y' || answer === 'yes') {
      execSync('npm init -y', { cwd, stdio: 'pipe' });
      ok('Created package.json');
    } else {
      fail('Create a package.json first, then re-run re-agent init.');
      process.exit(1);
    }
  }

  const pkg = readJson(pkgPath);
  let pkgDirty = false;

  // ── 2. lefthook dependency ────────────────────────────────────────────────

  if (!pkg.devDependencies) pkg.devDependencies = {};
  const current = pkg.devDependencies.lefthook;

  if (!current) {
    pkg.devDependencies.lefthook = LEFTHOOK_VERSION;
    pkgDirty = true;
    ok(`Adding lefthook@${LEFTHOOK_VERSION} to devDependencies`);
  } else {
    const cmp = semverCompare(current, LEFTHOOK_VERSION);
    if (cmp < 0) {
      const answer = await prompt(
        `  ${yellow(`lefthook@${current} found.`)} Bump to ${bold(LEFTHOOK_VERSION)}? (y/n) `
      );
      if (answer === 'y' || answer === 'yes') {
        pkg.devDependencies.lefthook = LEFTHOOK_VERSION;
        pkgDirty = true;
        ok(`Bumped lefthook to ${LEFTHOOK_VERSION}`);
      } else {
        skip(`Keeping lefthook@${current}`);
      }
    } else {
      skip(`lefthook@${current} is current`);
    }
  }

  // ── 3. prepare script ────────────────────────────────────────────────────

  if (!pkg.scripts) pkg.scripts = {};
  const prepare = pkg.scripts.prepare || '';

  if (!prepare) {
    pkg.scripts.prepare = 'lefthook install || true';
    pkgDirty = true;
    ok('Added prepare script');
  } else if (!prepare.includes('lefthook install')) {
    pkg.scripts.prepare = `${prepare} && (lefthook install || true)`;
    pkgDirty = true;
    ok('Appended lefthook install to prepare script');
  } else {
    skip('prepare script already includes lefthook install');
  }

  if (pkgDirty) {
    writeJson(pkgPath, pkg);
  }

  // ── 4. Consolidate existing agent config files ───────────────────────────

  const agentsPath = join(cwd, 'AGENTS.md');
  const symlinkTargets = [
    { path: join(cwd, 'CLAUDE.md'), label: 'CLAUDE.md' },
    { path: join(cwd, '.github', 'copilot-instructions.md'), label: '.github/copilot-instructions.md' },
    { path: join(cwd, '.cursor', 'rules', 'main.mdc'), label: '.cursor/rules/main.mdc' },
  ];

  let hasAgents = existsSync(agentsPath);

  for (const { path: filePath, label } of symlinkTargets) {
    if (!existsSync(filePath)) continue;
    if (lstatSync(filePath).isSymbolicLink()) continue;

    const content = readFileSync(filePath, 'utf8');

    if (!hasAgents) {
      renameSync(filePath, agentsPath);
      hasAgents = true;
      ok(`Moved ${label} → AGENTS.md`);
    } else {
      const existing = readFileSync(agentsPath, 'utf8');
      unlinkSync(filePath);
      if (content !== existing) {
        warn(`Removed ${label} (had different content — review AGENTS.md)`);
      } else {
        ok(`Removed ${label} (duplicate of AGENTS.md)`);
      }
    }
  }

  // Consolidate .claude/commands → .agent/skills
  const claudeCommandsDir = join(cwd, '.claude', 'commands');
  const agentSkillsDir = join(cwd, '.agent', 'skills');

  if (existsSync(claudeCommandsDir)) {
    for (const entry of readdirSync(claudeCommandsDir)) {
      const src = join(claudeCommandsDir, entry);
      if (lstatSync(src).isSymbolicLink()) continue;
      const dest = join(agentSkillsDir, entry);
      if (existsSync(dest)) {
        warn(`.agent/skills/${entry} already exists — skipping .claude/commands/${entry}`);
        continue;
      }
      mkdirSync(agentSkillsDir, { recursive: true });
      renameSync(src, dest);
      ok(`Moved .claude/commands/${entry} → .agent/skills/${entry}`);
    }
  }

  // Consolidate .planning/codebase → .agent/codebase
  const planningCodebase = join(cwd, '.planning', 'codebase');
  const agentCodebase = join(cwd, '.agent', 'codebase');

  if (existsSync(planningCodebase) && !lstatSync(planningCodebase).isSymbolicLink()) {
    if (!existsSync(agentCodebase)) {
      mkdirSync(join(cwd, '.agent'), { recursive: true });
      renameSync(planningCodebase, agentCodebase);
      ok('Moved .planning/codebase → .agent/codebase');
    }
  }

  // ── 5. Bootstrap AGENTS.md and .agent/ ───────────────────────────────────

  if (!hasAgents) {
    writeFileSync(agentsPath, AGENTS_TEMPLATE);
    ok('Created AGENTS.md');
  } else {
    skip('AGENTS.md already exists');
  }

  const skillsDir = join(cwd, '.agent', 'skills');
  const skillsGitkeep = join(skillsDir, '.gitkeep');
  if (!existsSync(skillsDir)) {
    mkdirSync(skillsDir, { recursive: true });
    writeFileSync(skillsGitkeep, '');
    ok('Created .agent/skills/');
  } else if (readdirSync(skillsDir).length === 0) {
    writeFileSync(skillsGitkeep, '');
  }

  // ── 6. lefthook.yml ──────────────────────────────────────────────────────

  const lefthookPath = join(cwd, 'lefthook.yml');
  const lefthookTemplate = readFileSync(join(TEMPLATES_DIR, 'lefthook.yml'), 'utf8');

  if (!existsSync(lefthookPath)) {
    writeFileSync(lefthookPath, lefthookTemplate);
    ok('Created lefthook.yml');
  } else {
    const existing = readFileSync(lefthookPath, 'utf8');
    if (existing.includes('setup-agent-symlinks.sh')) {
      skip('lefthook.yml already configured');
    } else if (existing.includes('post-checkout:')) {
      warn('lefthook.yml has a post-checkout section — add this manually:');
      console.log(dim('    commands:'));
      console.log(dim('      agent-symlinks:'));
      console.log(dim('        run: bash scripts/setup-agent-symlinks.sh'));
    } else {
      writeFileSync(lefthookPath, existing.trimEnd() + '\n\n' + lefthookTemplate);
      ok('Appended agent-symlinks hook to lefthook.yml');
    }
  }

  // ── 7. scripts/setup-agent-symlinks.sh ───────────────────────────────────

  const scriptsDir = join(cwd, 'scripts');
  const scriptPath = join(scriptsDir, 'setup-agent-symlinks.sh');
  const scriptTemplate = readFileSync(join(TEMPLATES_DIR, 'setup-agent-symlinks.sh'), 'utf8');

  if (!existsSync(scriptsDir)) {
    mkdirSync(scriptsDir, { recursive: true });
  }

  const existed = existsSync(scriptPath);
  writeFileSync(scriptPath, scriptTemplate);
  chmodSync(scriptPath, 0o755);
  ok(existed ? 'Updated scripts/setup-agent-symlinks.sh' : 'Created scripts/setup-agent-symlinks.sh');

  // ── 8. .gitignore ────────────────────────────────────────────────────────

  const gitignorePath = join(cwd, '.gitignore');

  if (!existsSync(gitignorePath)) {
    writeFileSync(gitignorePath, GITIGNORE_SECTION + '\n');
    ok('Created .gitignore with re-agent section');
  } else {
    let content = readFileSync(gitignorePath, 'utf8');

    if (content.includes('generated by re-agent')) {
      skip('.gitignore already has re-agent section');
    } else if (GITIGNORE_PATTERN.test(content)) {
      // Replace old "generated by ..." section with re-agent section
      content = content.replace(GITIGNORE_PATTERN, GITIGNORE_SECTION + '\n');
      writeFileSync(gitignorePath, content);
      ok('Replaced old agent symlinks section in .gitignore');
    } else {
      writeFileSync(gitignorePath, content.trimEnd() + '\n\n' + GITIGNORE_SECTION + '\n');
      ok('Added re-agent section to .gitignore');
    }
  }

  // ── 9. next steps ────────────────────────────────────────────────────────

  const pm = detectPackageManager(cwd);

  console.log(bold('\n  Done! Next steps:\n'));
  console.log(`  1. Run ${bold(`${pm} install`)} to install lefthook and set up git hooks`);
  console.log(`  2. Edit ${bold('AGENTS.md')} with your project's conventions`);
  console.log(`  3. Checkout a branch to trigger the symlinks\n`);
}

// ── CLI ─────────────────────────────────────────────────────────────────────

const command = process.argv[2];

if (command === 'init') {
  init().catch((err) => {
    console.error(red(`\nError: ${err.message}`));
    process.exit(1);
  });
} else {
  console.log(bold('\nre-agent') + dim(' — AI agent convention manager\n'));
  console.log('  Usage: re-agent init\n');
  console.log('  Sets up lefthook + symlinks to keep AI agent configs');
  console.log('  (Claude, Copilot, Cursor) aligned with AGENTS.md\n');
  process.exit(command ? 1 : 0);
}
